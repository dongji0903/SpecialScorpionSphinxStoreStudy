package com.sphinx.beautea.order.web.command;

public class OrderCommand {

	//넘겨 줄 값들
	private double totalPrice;
	private String recipient;
	private String recipientAddr;
	private String memo;
	//
	private String recipient2;
	private String sample4_postcode;
	private String sample4_roadAddress;
	private String area2;
	
	
	public double getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(double totalPrice) {
		this.totalPrice = totalPrice;
	}
	public String getRecipient() {
		return recipient + "/" + recipient2;
	}
	public void setRecipient(String recipient) {
		this.recipient = recipient;
	}
	public String getRecipientAddr() {
		recipientAddr = sample4_roadAddress + "/" + area2 + "/" + sample4_postcode;
		return recipientAddr;
	}
	public void setRecipientAddr(String recipientAddr) {
		this.recipientAddr = recipientAddr;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public void setRecipient2(String recipient2) {
		this.recipient2 = recipient2;
	}
	public String getPost() {
		return sample4_postcode;
	}
	public void setPost(String post) {
		this.sample4_postcode = post;
	}
	public String getArea1() {
		return sample4_roadAddress;
	}
	public void setArea1(String area1) {
		this.sample4_roadAddress = area1;
	}
	public String getArea2() {
		return area2;
	}
	public void setArea2(String area2) {
		this.area2 = area2;
	}
	public String getRecipient2() {
		return recipient2;
	}
	
	
}
